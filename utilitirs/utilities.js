
document.getElementById('btn-cloud').addEventListener('click', function(){
  alert('Board Updated successfully');
  const taskNumber = document.getElementById('task-number');
  const conTaskNumber = parseInt(taskNumber.innerText);
  const finalTaskNumber = conTaskNumber-1;
  taskNumber.innerText = finalTaskNumber;
  const navNumber = document.getElementById('nav-number');
  const conNavNumber = parseInt(navNumber.innerText);
  const finalNavNumber = conNavNumber+1;
  navNumber.innerText = finalNavNumber;
  const btn = document.getElementById('btn-cloud');
  btn.style.opacity = '0.2';
  btn.style.pointerEvents = 'none';
  if(finalNavNumber>28){
    alert('congratulations!! You have completed all the task')

  }
  
  
})

